package package1;

public class SwapNumber 
{
	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int c;
		
		System.out.println(" a= "+a);
		System.out.println(" b= "+b);
	
		c=a;
		a=b;
		b=c;
		System.out.println("swap values of a and b is");
		System.out.println(" a= "+a);
		System.out.println(" b= "+b);
	}

}
